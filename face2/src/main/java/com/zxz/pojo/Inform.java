package com.zxz.pojo;

public class Inform {
	private Integer inform_id;
	private String i_topic;
	private String i_article;
	private String i_date;
	private String i_name;
	public Integer getInform_id() {
		return inform_id;
	}
	public void setInform_id(Integer inform_id) {
		this.inform_id = inform_id;
	}
	public String getI_topic() {
		return i_topic;
	}
	public void setI_topic(String i_topic) {
		this.i_topic = i_topic;
	}
	public String getI_article() {
		return i_article;
	}
	public void setI_article(String i_article) {
		this.i_article = i_article;
	}
	public String getI_date() {
		return i_date;
	}
	public void setI_date(String i_date) {
		this.i_date = i_date;
	}
	public String getI_name() {
		return i_name;
	}
	public void setI_name(String i_name) {
		this.i_name = i_name;
	}
	

}
